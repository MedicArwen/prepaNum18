
            <h2>Animaux éteints dans le monde</h2>
            <h3>Le rhinocéros blanc</h3>
            <p>Le dernier rhinocéros (mâle) blanc s’est éteint en mars dernier à l’âge de 45 ans. Il s’agissait de l’unique survivant de son espèce. Sa disparition est due aux braconnages importants entre les années 1970 et 1980.</p>
