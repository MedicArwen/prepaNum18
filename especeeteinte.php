            <h3>Le dodo</h3>
            <p>Si vous avez déjà mis les pieds sur l’île Maurice, vous n’êtes pas sans savoir que le dodo est l’emblème national. Cet étrange pélican géant a malheureusement été exterminé par les colons et marins, qui ont envahi l’île sauvage en s’appropriant les fruits de sa nature luxuriante.
